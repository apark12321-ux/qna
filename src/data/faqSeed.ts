function slug(value: string) {
  return value.replace(/[^0-9A-Za-z가-힣]+/g, '-').replace(/^-|-$/g, '').toLowerCase();
}

function question(topic: string, index: number) {
  return patterns[index % patterns.length].replace('{topic}', topic);
}

function body(category: FaqCategory, topic: string, q: string) {
  const source = sourceByCategory[category];
  return `# ${q}\n\n## 답변\n${topic}은 먼저 본인 상황, 신청 대상, 처리 기한, 필요 서류를 확인한 뒤 진행하면 됩니다. 세부 기준은 시기와 기관에 따라 달라질 수 있으므로 최종 확인은 ${source}에서 해야 합니다.\n\n## 확인 순서\n1. 내 상황이 해당되는지 확인합니다.\n2. 필요한 서류와 본인 인증 수단을 준비합니다.\n3. 온라인 신청 가능 여부를 확인합니다.\n4. 접수번호, 신청일, 담당 기관을 기록합니다.\n5. 결과가 다르면 사유를 확인하고 보완합니다.\n\n## 체크리스트\n- 신청자 본인 또는 대리 신청 가능 여부\n- 기간, 금액, 주소지, 계약일, 소득 기준\n- 제출 서류의 발급일과 유효기간\n- 수수료, 환급, 위약금, 처리기한\n- 공식 확인처와 문의 번호\n\n## FAQ\n### Q. 블로그 글만 보고 처리해도 되나요?\n아니요. 참고는 가능하지만 최종 신청이나 계약 전에는 공식 안내를 확인해야 합니다.\n\n### Q. 가장 먼저 볼 것은 무엇인가요?\n마감일, 신청 자격, 서류 누락 여부를 먼저 확인해야 합니다.\n\n### Q. 상황이 애매하면 어떻게 하나요?\n관련 서류를 준비한 뒤 담당 기관에 문의하는 것이 안전합니다.`;
}

export const seedFaqArticles: SeedFaqArticle[] = faqCategories.flatMap((category) => {
  const list = topics[category];
  return Array.from({ length: 100 }, (_, index) => {
    const topic = list[index % list.length];
    const q = question(topic, index);
    return {
      id: `seed-${slug(category)}-${index + 1}`,
      question: q,
      title: q,
      summary: `${topic} 관련 확인 순서, 체크리스트, FAQ를 정리했습니다.`,
      category,
      body: body(category, topic, q),
      createdAt: '2026-06-20T00:00:00.000Z',
      helpful: 0,
      isSeed: true
    };
  });
});

